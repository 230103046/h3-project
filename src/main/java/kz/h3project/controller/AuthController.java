package kz.h3project.controller;

import jakarta.validation.Valid;
import kz.h3project.model.user.dto.LoginRequest;
import kz.h3project.model.user.dto.RegisterRequest;
import kz.h3project.model.user.entity.Role;
import kz.h3project.model.user.entity.User;
import kz.h3project.model.user.enums.RoleDic;
import kz.h3project.repository.RoleRepository;
import kz.h3project.repository.UserRepository;
import kz.h3project.service.TokenService;
import lombok.RequiredArgsConstructor;
import org.springframework.http.ResponseEntity;
import org.springframework.security.authentication.BadCredentialsException;
import org.springframework.security.crypto.password.PasswordEncoder;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

import java.util.Map;
import java.util.Set;

@RestController
@RequestMapping("/auth")
@RequiredArgsConstructor
public class AuthController {

    private final TokenService tokenService;
    private final UserRepository userRepository;
    private final PasswordEncoder passwordEncoder;
    private final RoleRepository roleRepository;

    @PostMapping("/login")
    public ResponseEntity<Map<String, Object>> login(@RequestBody LoginRequest request) {
        User byUsername = userRepository.findByUsername(request.getUsername())
                .orElseThrow(() -> new BadCredentialsException("Invalid credentials"));

        if (!passwordEncoder.matches(request.getPassword(), byUsername.getPassword())) {
            throw new BadCredentialsException("Invalid credentials");
        }

        String token = tokenService.createToken(byUsername);

        return ResponseEntity.ok(Map.of("token", token));
    }

    @PostMapping("/register")
    public ResponseEntity<Map<String, Object>> register(@Valid @RequestBody RegisterRequest request) {
        if (userRepository.existsByUsername(request.getUsername())) {
            return ResponseEntity.badRequest()
                    .body(Map.of("error", "Username already exists"));
        }

        if (userRepository.existsByEmail(request.getEmail())) {
            return ResponseEntity.badRequest()
                    .body(Map.of("error", "Email already exists"));
        }

        Role userRole = roleRepository.findByName(RoleDic.USER.name())
                .orElseThrow(() -> new RuntimeException("USER role not found"));

        User user = User.builder()
                .username(request.getUsername())
                .password(passwordEncoder.encode(request.getPassword()))
                .email(request.getEmail())
                .enabled(true)
                .roles(Set.of(userRole))
                .build();

        userRepository.save(user);

        String token = tokenService.createToken(user);

        Map<String, Object> response = Map.of(
                "message", "User registered successfully",
                "token", token,
                "username", request.getUsername(),
                "email", request.getEmail()
        );

        return ResponseEntity.ok(response);
    }
}

